// Check if a number is positive, negative, or zero
// Want to try that one?
function checknum(num) {
    if (num > 0){
        return `${num} is positive`
    }
    else if (num < 0){
        return `${num} is negative`
    }
    else if (num == 0){
        return `it is ${num} `
    }
}

console.log(checknum(1.4));






